# if - else - nested use for multiple conditions

x=int(input('enter a number '))
y=int(input('enter a number '))
if x>0:
    print(" x positive")
else:
    print("x non positive")
if y>0:
    print("y positive")
else:
    print("y non positive")
if x==y:
    print("x equal y")
else:
    print("x not equal y")